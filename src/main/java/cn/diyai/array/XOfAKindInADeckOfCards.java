    //fixme
    //x一定能整除deck的长度n。而且x也一定能整除group里每一个元素个数
    public boolean hasGroupsSizeX(int[] deck) {
        int size = deck.length;
        if(size <= 1){
            return false;
        }

        //冒泡排序
        int temp;
        for(int i = 0 ; i < size;i++){
            for(int j = i ; j < size;j++){
                if(deck[i] > deck[j]){
                    temp = deck[i];
                    deck[i] = deck[j];
                    deck[j] = temp;
                }
            }
        }

        int sameCount = 1;
        int currentVal = deck[0];
        //组成员个数
        int groupElementCount = 1;
        boolean isSetGroupElementCount = false;
        for(int i = 1 ; i < size; i++){
            //统计相同元素个数
            if(deck[i] != currentVal){

                if(sameCount < 2){
                    return false;
                }

                //不能构成一组
//                if(sameCount % groupElementCount != 0 || groupElementCount % sameCount != 0){
//                    return false;
//                }

                if(!isSetGroupElementCount && groupElementCount % 2 == 0){
                    groupElementCount = 2;
                }
                isSetGroupElementCount = true;
                currentVal =  deck[i];
                sameCount = 1;
            }else{
                sameCount++;
                //确定组元素的个数
                if(!isSetGroupElementCount){
                    groupElementCount = sameCount;
                }
            }
        }

        if(sameCount == 1){
            return false;
        }


        if(sameCount % groupElementCount != 0 && groupElementCount % sameCount != 0){
            return false;
        }

        return true;
    }

